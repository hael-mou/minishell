#include "libft.h"

//====<[ ft_lstlast: ]>=========================================================
t_list	*ft_lstlast(t_list *lst)
{
	while (lst)
	{
		if (lst->next == 0)
			return (lst);
		lst = lst->next;
	}
	return (0);
}
