#include "libft.h"

//====<[ ft_lstadd_back: ]>=====================================================
void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*med;

	if (*lst)
	{
		med = ft_lstlast(*lst);
		med->next = new;
	}
	else
		(*lst) = new;
}
