//===<[ get_node_type: ]>=======================================================
int	get_node_type(void *node)
{
	t_node	*g_node;

	g_node = node;
	if (g_node != NULL)
		return (g_node->type);
	return (ERROR);
}

//===<[ waitnode: ]>============================================================
int	waitnode(t_node *node, int *sig)
{
	waitpid(node->pid, &node->status, 0);
	*sig = ((WTERMSIG(node->status) == 2) || (*sig == TRUE));
	return ((((*(int *)&(node->status)) >> 8) & 0x000000ff));
}
