#include "parser.h"

//===<[ parser: ]>==============================================================
t_node	*parser(t_list *tokens)
{
	t_node	*tree;
	void	*copy;

	copy = tokens;
	g_sys.area = PARSER_AREA;
	minish_sigign();
	if (tokens == NULL)
		return (NULL);
	tree = parse_seq(&tokens);
	minishell_signal();
	clean_tokenizer(copy);
	return (tree);
}
